package jianzhaozhe;

public class Meal {//定义两个属性，相当于product      顺序1
    private String food;
    private String drinks;
    public String getFood() {
        return food;
    }

    public void setFood(String food) {
        this.food = food;
    }

    public String getDrinks() {
        return drinks;
    }

    public void setDrinks(String drinks) {
        this.drinks = drinks;
    }


}